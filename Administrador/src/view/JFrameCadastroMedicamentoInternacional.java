    private void JButtonConcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonConcluirActionPerformed
        if (jComboBoxPaisOrigem.getSelectedIndex() == -1) {
            Icon icone = new ImageIcon(getToolkit().createImage(getClass().getResource("/icones/alerta.png")));
            JOptionPane.showMessageDialog(null, "Deve ser selecionado algum pais de origem!!!", "Aviso", JOptionPane.ERROR_MESSAGE, iconeAlerta);
            return;
        }
        if (jComboBoxPaisDestino.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Deve ser selecionado algum pais de destino!!!", "Aviso", JOptionPane.ERROR_MESSAGE, iconeAlerta);
            return;
        }
        if (jComboBoxMedicamentoOrigem.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Deve ser selecionado algum medicamento de origem!!!", "Aviso", JOptionPane.ERROR_MESSAGE, iconeAlerta);
            return;
        }
        if (JComboBoxMedicamentoDestino.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Deve ser selecionado algum medicamento de destino!", "Aviso", JOptionPane.ERROR_MESSAGE, iconeAlerta);
            return;
        } else {
            Medicamento medicamentoOrigem = (Medicamento) jComboBoxMedicamentoOrigem.getSelectedItem();
            Medicamento medicamentoDestino = (Medicamento) JComboBoxMedicamentoDestino.getSelectedItem();
            int valor = MedicamentoInternacionalDAO.inserir(medicamentoOrigem, medicamentoDestino);
            JOptionPane.showMessageDialog(null, "Medicamento foi inserido!", "Aviso", JOptionPane.ERROR_MESSAGE, iconeCerto);
            dispose();
        }
    }//GEN-LAST:event_JButtonConcluirActionPerformed

