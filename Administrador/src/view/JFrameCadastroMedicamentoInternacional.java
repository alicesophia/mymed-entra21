    private void jComboBoxPaisOrigemItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBoxPaisOrigemItemStateChanged
        Pais paisOrigem = (Pais) jComboBoxPaisOrigem.getSelectedItem();
        if (paisOrigem != null) {
            ArrayList<Medicamento> medicamentos = (ArrayList) MedicamentoDAO.listarMedicamentos(paisOrigem.getId());
            jComboBoxMedicamentoOrigem.removeAllItems();
            for (Medicamento medicamento : medicamentos) {
                jComboBoxMedicamentoOrigem.addItem(medicamento);
            }
            jComboBoxMedicamentoOrigem.setSelectedIndex(-1);
        }
    }//GEN-LAST:event_jComboBoxPaisOrigemItemStateChanged

