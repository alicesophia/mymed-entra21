    private void listarDados() {
        ArrayList<Farmacia> farmacias = (ArrayList<Farmacia>) FarmaciaDAO.retornarFarmaciasSemLimites();
        DefaultTableModel dtm = (DefaultTableModel) TabelaFarmacias.getModel();
        dtm.setRowCount(0);
        for (int i = 0; i < farmacias.size(); i++) {
            Farmacia farmacia = farmacias.get(i);
            dtm.addRow(new Object[]{
                farmacia.getId(),
                farmacia.getNome(),
                farmacia.getCidade().getNome(),
                farmacia.getTelefone(),
                farmacia.getSite()
            });
        }
    }

