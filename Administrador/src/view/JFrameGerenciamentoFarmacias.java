    private void JButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonEditarActionPerformed
        if (TabelaFarmacias.getSelectedRow() > -1) {
            int linhaSelecionada = TabelaFarmacias.getSelectedRow();
            int codigoParaAlterar = Integer.parseInt(TabelaFarmacias.getValueAt(linhaSelecionada, 0).toString());
            JFrameAlterarFarmacia alterar = new JFrameAlterarFarmacia(codigoParaAlterar);
            alterar.setVisible(true);
        } else {
            Icon icone = new ImageIcon(getToolkit().createImage(getClass().getResource("/icones/alerta.png")));
            JOptionPane.showMessageDialog(null, "Não foi selecionado nenhum item da tabela!", "Aviso!", JOptionPane.ERROR_MESSAGE, icone);
        }
    }//GEN-LAST:event_JButtonEditarActionPerformed

    private void JButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonExcluirActionPerformed
        if (TabelaFarmacias.getSelectedRow() > -1) {
            int linhaSelecionada = TabelaFarmacias.getSelectedRow();
            int codigoParaExcluir = Integer.parseInt(TabelaFarmacias.getValueAt(linhaSelecionada, 0).toString());
            DefaultTableModel dtm = (DefaultTableModel) TabelaFarmacias.getModel();
            FarmaciaDAO.excluir(codigoParaExcluir);
            dtm.removeRow(linhaSelecionada);
        } else {
            Icon icone = new ImageIcon(getToolkit().createImage(getClass().getResource("/icones/alerta.png")));
            JOptionPane.showMessageDialog(null, "Não foi selecionado nenhum item da tabela!", "Aviso!", JOptionPane.ERROR_MESSAGE, icone);
        }

    }//GEN-LAST:event_JButtonExcluirActionPerformed

