    private void listarDados() {
        ArrayList<Usuario> users = (ArrayList<Usuario>) UsuarioDAO.retornarUsuario();
        DefaultTableModel dtm = (DefaultTableModel) TabelaUsuarios.getModel();
        dtm.setRowCount(0);
        for (int i = 0; i < users.size(); i++) {
            Usuario user = users.get(i);
            dtm.addRow(new Object[]{
                user.getId(),
                user.getNome(),
                user.getUsuario(),
                user.getEmail(),
                user.getSenha()
            });
        }
    }
