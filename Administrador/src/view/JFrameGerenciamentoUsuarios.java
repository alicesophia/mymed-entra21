    private void JButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonEditarActionPerformed
        if (TabelaUsuarios.getSelectedRow() > -1) {
            int linhaSelecionada = TabelaUsuarios.getSelectedRow();
            int codigoParaAlterar = Integer.parseInt(TabelaUsuarios.getValueAt(linhaSelecionada, 0).toString());
            JFrameAlterarUsuario alterar = new JFrameAlterarUsuario(codigoParaAlterar);
            alterar.setVisible(true);
        } else {
            Icon icone = new ImageIcon (getToolkit().createImage(getClass().getResource("/icones/alerta.png")));
            JOptionPane.showMessageDialog(null, "Não foi selecionado nenhum item na tabela","Aviso",JOptionPane.ERROR_MESSAGE,icone);
        }
    }//GEN-LAST:event_JButtonEditarActionPerformed

    private void JButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonExcluirActionPerformed
        if (TabelaUsuarios.getSelectedRow() > -1) {
            int linhaSelecionada = TabelaUsuarios.getSelectedRow();
            int codigoParaExcluir = Integer.parseInt(TabelaUsuarios.getValueAt(linhaSelecionada, 0).toString());
            DefaultTableModel dtm = (DefaultTableModel) TabelaUsuarios.getModel();
            UsuarioDAO.excluir(codigoParaExcluir);
            dtm.removeRow(linhaSelecionada);
        } else {
            Icon icone = new ImageIcon (getToolkit().createImage(getClass().getResource("/icones/alerta.png")));
            JOptionPane.showMessageDialog(null, "Não foi selecionado nenhum item da tabela","Aviso",JOptionPane.ERROR_MESSAGE,icone);
        }
    }//GEN-LAST:event_JButtonExcluirActionPerformed
