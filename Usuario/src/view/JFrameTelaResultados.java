    public JFrameTelaResultados(PaisUser paisOrigem, PaisUser paisDestino, String nomeMedicamento) {
        initComponents();
        ArrayList<MedicamentoUser> medicamentos = MedicamentoInternacionalDAOUser.buscarMedicamentosInternacionais(paisOrigem, paisDestino, nomeMedicamento);
        DefaultTableModel dtm = (DefaultTableModel) TabelaResultados.getModel();
        dtm.setRowCount(0);
        for (int i = 0; i < medicamentos.size(); i++) {
            MedicamentoUser medicamento = medicamentos.get(i);
            dtm.addRow(new Object[]{
                medicamento.getId(),
                medicamento.getParaQServe(),
                medicamento.getNome(),
                medicamento.getPreco()
            });
        }
    }

