    private void jButtonSaibaMaisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaibaMaisActionPerformed
        if (TabelaResultados.getSelectedRow() > -1) {
            //dispose();
            int linhaSelecionada = TabelaResultados.getSelectedRow();
            int codigoParaDescricao = Integer.parseInt(TabelaResultados.getValueAt(linhaSelecionada, 0).toString());
            JFrameDescricao descricao = new JFrameDescricao(codigoParaDescricao);
            descricao.setVisible(true);
        } else {
            Icon icone = new ImageIcon(getToolkit().createImage(getClass().getResource("/icones/alerta.png")));
            JOptionPane.showMessageDialog(null, "Não foi selecionado nenhum item na tabela", "Aviso", JOptionPane.ERROR_MESSAGE, icone);
        }
    }//GEN-LAST:event_jButtonSaibaMaisActionPerformed

