    private void JButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonCadastrarActionPerformed
        if (JTextFieldNome.getText().trim().equals("")) {
            Icon icone = new ImageIcon(getToolkit().createImage(getClass().getResource("/Icones/alerta.png")));
            JOptionPane.showMessageDialog(null, "Algum usu√°rio deve ser digitado!!!", "Aviso", JOptionPane.ERROR_MESSAGE, icone);
            return;
        }
        if (JTextFieldEmail.getText().trim().equals("")) {
            Icon icone = new ImageIcon(getToolkit().createImage(getClass().getResource("/Icones/alerta.png")));
            JOptionPane.showMessageDialog(null, "Algum email deve ser digitado!!!", "Aviso", JOptionPane.ERROR_MESSAGE, icone);
            return;
        }
        if (new String(JPasswordSenha.getPassword()).trim().equals("")) {
            Icon icone = new ImageIcon(getToolkit().createImage(getClass().getResource("/Icones/alerta.png")));
            JOptionPane.showMessageDialog(null, "Deve ser digitada alguma senha!!!", "Aviso", JOptionPane.ERROR_MESSAGE, icone);
            return;
        }
        if (new String(JPasswordConfirmar.getPassword()).trim().equals("")) {
            Icon icone = new ImageIcon(getToolkit().createImage(getClass().getResource("/Icones/alerta.png")));
            JOptionPane.showMessageDialog(null, "Deve ser confirmada a senha!!", "Aviso", JOptionPane.ERROR_MESSAGE, icone);
            return;
        } else {
            dispose();
            UsuarioUser user = new UsuarioUser();
            user.setNome(JTextFieldNome.getText());
            user.setUsuario(JTextFieldUsuario.getText());
            user.setSenha(new String(JPasswordConfirmar.getPassword()));
            user.setEmail(JTextFieldEmail.getText());
            user.setId(UsuarioDAOUser.inserir(user));
            codigo = user.getId();
            Icon icone = new ImageIcon(getToolkit().createImage(getClass().getResource("/Icones/alerta.png")));
            JOptionPane.showMessageDialog(null, "Usu√°rio foi cadastrado!!!", "Aviso", JOptionPane.ERROR_MESSAGE, icone);
        }
    }//GEN-LAST:event_JButtonCadastrarActionPerformed

