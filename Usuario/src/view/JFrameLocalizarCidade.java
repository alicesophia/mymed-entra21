    private void jButtonLocalizarFarmaciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLocalizarFarmaciaActionPerformed
        if (jTableCidades.getSelectedRow() > -1) {
            //dispose();
            int linhaSelecionada = jTableCidades.getSelectedRow();
            int codigoParaDescricao = Integer.parseInt(jTableCidades.getValueAt(linhaSelecionada, 0).toString());
            new JFrameFarmaciasProximas(codigoParaDescricao).setVisible(true);
        } else {
            Icon icone = new ImageIcon(getToolkit().createImage(getClass().getResource("/icones/alerta.png")));
            JOptionPane.showMessageDialog(null, "Não foi selecionado nenhum item na tabela", "Aviso", JOptionPane.ERROR_MESSAGE, icone);
        }
    }//GEN-LAST:event_jButtonLocalizarFarmaciaActionPerformed

