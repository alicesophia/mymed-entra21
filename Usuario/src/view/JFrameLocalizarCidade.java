    public JFrameLocalizarCidade(String nome) {
        initComponents();
        ArrayList<CidadeUser> cidades = CidadeDAOUser.resultadosDaPesquisa(nome);
        DefaultTableModel dtm = (DefaultTableModel) jTableCidades.getModel();
        dtm.setRowCount(0);
        for (int i = 0; i < cidades.size(); i++) {
            cidade = cidades.get(i);
            dtm.addRow(new Object[]{
                cidade.getId(),
                cidade.getNome()
            });
            
        }
    }

